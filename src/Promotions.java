    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
                PreparedStatement ps= null;
                ResultSet rs= null;
                String ItemName=String.valueOf(jComboBox1.getSelectedItem());
                String code=jTextField4.getText();
                SimpleDateFormat sdf= new SimpleDateFormat("yyyy-MM-dd");
                String frm=sdf.format(jDateChooser1.getDate());
                String till=sdf.format(jDateChooser2.getDate());
                String newPrice=jTextField1.getText();
                String Status=String.valueOf(jComboBox2.getSelectedItem());
                
                if(ItemName.equals("") || code.equals("") || frm.equals("") || till.equals("") || newPrice.equals("") || Status.equals("") ){
            JOptionPane.showMessageDialog(this, "Please Fill all Fields!");
            this.setVisible(false);
                }else{
                    try {
                        String query="INSERT INTO `promotions`"+
                                "(`ItemName`, `Code`, `frm`, `till`, `newPrice`,`Status`)"+
                                "VALUES(?,?,?,?,?,?)";
                        ps=DBConnection.con().prepareStatement(query);
                        ps.setString(1,ItemName);
                        ps.setString(2,code);
                        ps.setString(3,frm);
                        ps.setString(4,till);
                        ps.setString(5,newPrice);
                        ps.setString(6,Status);
                        
                         ps.execute();

                    JOptionPane.showMessageDialog(this, "Done!");
                    DisplayTable();
                    jComboBox1.setSelectedItem(null);
                    jComboBox2.setSelectedItem(null);
                    jTextField4.setText(null);
                    jTextField1.setText(null);
                    jDateChooser1.setDate(null);
                    jDateChooser2.setDate(null);
                    
                    } catch (SQLException ex) {
                        Logger.getLogger(Promotions.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
    }//GEN-LAST:event_jButton13ActionPerformed

