    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String a= jTextField4.getText();
            String query ="SELECT  * FROM  `promotions`  WHERE Code = ? ";
            PreparedStatement ps=null;
            ResultSet rs=null;
            ps=DBConnection.con().prepareStatement(query);
            ps.setString(1, a);
            rs=ps.executeQuery();
            if(rs.next()){

                String dte1=(rs.getString("frm"));
                java.util.Date date= new SimpleDateFormat("yyyy-MM-dd").parse(dte1);
                String dte2=(rs.getString("till"));
                java.util.Date date2= new SimpleDateFormat("yyyy-MM-dd").parse(dte2);
               jDateChooser1.setDate(date);
                jDateChooser2.setDate(date2);
                jTextField1.setText(rs.getString("newPrice"));
                jComboBox2.setSelectedItem(rs.getString("newPrice"));
            }
            else{
                JOptionPane.showMessageDialog(this, "Item Not Found!");
                jTextField4.setText("");
            }
        } catch (SQLException ex) {
            Logger.getLogger(CUSTOMER.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(Promotions.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

